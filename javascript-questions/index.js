const superHeroes = require('./super-heroes.json');

/**
 * Will find all of the marvel characters in the data and return them as an array
 * @returns [
 *  {
 *    ...hero,
 *    publisher: 'Marvel'
 *  }
 * ]
 */
const getDCHeroes = (heroes) => {
  heroes = heroes.filter(hero => hero.publisher.includes('DC'))
  return heroes;
}

/**
 * Converts the character property into an array
 * @returns [
 *  {
 *    ...hero,
 *    character: [
 *      string,
 *      string,
 *      string,
 *    ]
 *  }
 * ]
 */
const convertCharactersToArray = (heroes) => {
  heroes = heroes.map(hero => {
    hero.characters = hero.characters.split(",")
    return hero;
  })
  return heroes;
}

/**
 * Coverts super hero data so it is grouped the publisher property
 * @returns {
 *   "publisher 1": [
 *     { ...hero }
 *     ...
 *   ],
 *   "publisher 2" :[
 *     {... hero}
 *     ...
 *   ]
 * }
 */
const groupByPublisher = (heroes) => {
  let heroes1 = heroes.filter(hero => hero.publisher.includes('DC'))
  let heroes2 = heroes.filter(hero => hero.publisher.includes('Marvel'))
  heroes = {
    "DC Comics" : heroes1,
    "Marvel Comics" : heroes2
  };
  return heroes;
}

/**
 * Returns and array of DC Comic heroes that have more than 1 character
 * @returns [
 *  {
 *    ...hero,
 *    character: [
 *      string,
 *      string,
 *      string,
 *    ]
 *  }
 * ]
 */
const getDCHeroesWithMoreThanOneCharacter = (heroes) => {
  let dcHeroes =  getDCHeroes(heroes);
  heroes = dcHeroes.filter(hero => hero.characters.length > 1)
  return heroes;
}

module.exports = {
  convertCharactersToArray,
  getDCHeroes,
  getDCHeroesWithMoreThanOneCharacter,
  groupByPublisher,
}
